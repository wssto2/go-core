package go2ts_test

import (
	"os"
	"path/filepath"
	"strings"
	"testing"

	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/require"
	"github.com/wssto2/go-core/go2ts"
)

// --- test types ---

type SimpleUser struct {
	ID   int    `json:"id"`
	Name string `json:"name"`
	Age  int    `json:"age,omitempty"`
}

type WithPointer struct {
	ID    int     `json:"id"`
	Score *float64 `json:"score"`
}

type Nested struct {
	ID      int        `json:"id"`
	Address AddressDTO `json:"address"`
}

type AddressDTO struct {
	Street string `json:"street"`
	City   string `json:"city"`
}

type WithSlice struct {
	Tags []string `json:"tags"`
}

type WithBool struct {
	Active bool `json:"active"`
}

type WithSkipped struct {
	ID     int    `json:"id"`
	Secret string `json:"-"`
}

// --- tests ---

func TestGenerateTypes_SimpleStruct(t *testing.T) {
	dir := t.TempDir()
	require.NoError(t, go2ts.GenerateTypes([]interface{}{SimpleUser{}}, dir))

	content, err := os.ReadFile(filepath.Join(dir, "SimpleUser.ts"))
	require.NoError(t, err)
	src := string(content)

	assert.Contains(t, src, "export type SimpleUser")
	assert.Contains(t, src, "id: number")
	assert.Contains(t, src, "name: string")
	// omitempty → optional field
	assert.Contains(t, src, "age?: number")
}

func TestGenerateTypes_PointerFieldIsNullable(t *testing.T) {
	dir := t.TempDir()
	require.NoError(t, go2ts.GenerateTypes([]interface{}{WithPointer{}}, dir))

	content, err := os.ReadFile(filepath.Join(dir, "WithPointer.ts"))
	require.NoError(t, err)
	src := string(content)

	assert.Contains(t, src, "score: number | null")
}

func TestGenerateTypes_NestedStructCreatesChildFile(t *testing.T) {
	dir := t.TempDir()
	require.NoError(t, go2ts.GenerateTypes([]interface{}{Nested{}}, dir))

	// Parent file should exist
	parent, err := os.ReadFile(filepath.Join(dir, "Nested.ts"))
	require.NoError(t, err)
	assert.Contains(t, string(parent), "address: AddressDTO")
	assert.Contains(t, string(parent), "import type { AddressDTO }")

	// Child file should be generated
	child, err := os.ReadFile(filepath.Join(dir, "AddressDTO.ts"))
	require.NoError(t, err)
	assert.Contains(t, string(child), "street: string")
	assert.Contains(t, string(child), "city: string")
}

