func TestGenerateTypes_SkipsJsonDashFields(t *testing.T) {
	dir := t.TempDir()
	require.NoError(t, go2ts.GenerateTypes([]interface{}{WithSkipped{}}, dir))

	content, err := os.ReadFile(filepath.Join(dir, "WithSkipped.ts"))
	require.NoError(t, err)
	src := string(content)

	assert.Contains(t, src, "id: number")
	assert.NotContains(t, src, "Secret")
	assert.NotContains(t, src, "secret")
}

func TestGenerateTypes_OutputHasAutoGeneratedComment(t *testing.T) {
	dir := t.TempDir()
	require.NoError(t, go2ts.GenerateTypes([]interface{}{SimpleUser{}}, dir))

	content, err := os.ReadFile(filepath.Join(dir, "SimpleUser.ts"))
	require.NoError(t, err)
	assert.True(t, strings.HasPrefix(string(content), "// This file is auto-generated."))
}

func TestGenerateTypes_DeduplicatesNestedTypes(t *testing.T) {
	dir := t.TempDir()
	// Provide AddressDTO twice indirectly — should only generate one file
	require.NoError(t, go2ts.GenerateTypes([]interface{}{Nested{}, AddressDTO{}}, dir))

	// AddressDTO.ts should exist
	_, err := os.ReadFile(filepath.Join(dir, "AddressDTO.ts"))
	require.NoError(t, err)
}

func TestGenerateTypes_ErrorOnNonStruct(t *testing.T) {
	dir := t.TempDir()
	err := go2ts.GenerateTypes([]interface{}{"not a struct"}, dir)
	assert.Error(t, err)
}

func TestGenerateTypes_CreatesOutputDirectory(t *testing.T) {
	base := t.TempDir()
	dir := filepath.Join(base, "deep", "nested", "dir")
	require.NoError(t, go2ts.GenerateTypes([]interface{}{SimpleUser{}}, dir))

	_, err := os.Stat(dir)
	assert.NoError(t, err)
}
